class Ex03 {
    public static void main(String[] args) {
        System.out.println(10 + 3 * 4); // 22
        System.out.println((10 + 3) * 4); // 52
        System.out.println(10 + (3 * 4)); // 22
        System.out.println(23 / 4); // 5
        System.out.println(4 / 23); // 0
        System.out.println(23 % 4); // 3
        System.out.println(4 % 23); // 4
    }
}