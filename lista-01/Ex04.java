class Ex04 {
    static void operationA() {
        System.out.println(6 * (2 + 3) / (4 + 5)); // 3
    }

    static void operationB() {
        System.out.println((42 / 7) * 5 + 6 * 3 + 4); // 52
    }

    public static void main(String[] args) {
        operationA();
        operationB();
    }
}